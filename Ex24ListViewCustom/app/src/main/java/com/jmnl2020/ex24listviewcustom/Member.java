package com.jmnl2020.ex24listviewcustom;

public class Member {

    String name;
    String nation;
    int imgId;

    //생성(new)할 때 자동으로 실행되는 메소드
    //생성자 메소드

    public Member(String name, String nation, int imgId){

        this.name= name;
        this.nation= nation;
        this.imgId= imgId;

    }//생성자 클래스 Member end.

}
